#ifndef EXT_INT_PRIV_H_
#define EXT_INT_PRIV_H_


/******************************************************************************/
/*                             REGISTER ADDRESSES		    	     		  */
/******************************************************************************/

#define MCUCR	*(( volatile u8*) 0x55)
#define	GICR	*(( volatile u8*) 0x5B)
#define GIFR	*(( volatile u8*) 0x5A)
#define MCUCSR	*(( volatile u8*) 0x54)
#define SREG    *(( volatile u8*) 0x5F)

/******************************************************************************/
/*                       INT0 INTERRUPT STATES MACROS	    	         	  */
/******************************************************************************/

#define INT0_LOW	(0U)
#define INT0_LC		(1U)
#define INT0_FE		(2U)
#define INT0_RE		(3U)

/******************************************************************************/
/*                       INT1 INTERRUPT STATES MACROS    	     	    	  */
/******************************************************************************/
#define INT1_LOW	(0U)
#define INT1_LC		(1U)
#define INT1_FE		(2U)
#define INT1_RE		(3U)

/******************************************************************************/
/*                        INT2 INTERRUPT STATES MACROS	    	     		  */
/******************************************************************************/

#define INT2_FE		(0U)
#define INT2_RE		(1U)

/******************************************************************************/
/*                             INTERRUPT MASKS MACROS			     		  */
/******************************************************************************/

#define INT0_MASK	0xFC
#define INT1_MASK	0xF3

/******************************************************************************/
/*                             ON/OFF STATES MACROS				  	          */
/******************************************************************************/
#define INT_OFF		(0U)
#define INT_ON		(1U)

/******************************************************************************/
/*                             MCUCSR BITS MACROS  		    	     		  */
/******************************************************************************/

#define ISC2_BIT	(6U)

/******************************************************************************/
/*                             GICR BITS MACROS  		    	     		  */
/******************************************************************************/

#define	INT0_BIT	(6U)
#define	INT1_BIT	(7U)
#define INT2_BIT	(5U)

/*****************************************************************************/

#endif /* EXT_INT_PRIV_H_ */